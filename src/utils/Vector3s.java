public class Vector3s {
    public short x, y, z;

    public Vector3s() {
        x = 0;
        y = 0;
        z = 0;
    }

    public Vector3s(short x, short y, short z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
